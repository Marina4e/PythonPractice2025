# Оператори порівняння
1 == 1  # рівно
1 != 2  # не рівно
4 > 2  # більше
4 < 5  # менше
4 >= 4  # більше або рівно
5 <= 2  # менше або рівно

# Логічні оператори
# І
a = True
b = False
print('a and b is', a and b)

# АБО
a = True
b = False
print('a and b is', a or b)

# НІ
a = True
b = False
print('a and b is', not b)

# Оператори посвідчення особи
a = 3
b = 4
print(a is b)
print(a is not b)

# Оператори членства

print('ITVDN' in 'Best online course is ITVDN')
print('ITVDN' not in 'Best online course is ITVDN')
